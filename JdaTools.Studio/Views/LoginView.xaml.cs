    public partial class LoginView : UserControl
    {
        public LoginView()
        {
            InitializeComponent();
        }

        private void PasswordBox_PasswordChanged(object sender, RoutedEventArgs e)
        {
            if (DataContext != null)
            {
                var vm = (LoginViewModel)DataContext;
                var securePassword = ((PasswordBox)sender).SecurePassword;
                var lazyPassword = SecureStringToString(securePassword);
                vm.Password = lazyPassword;      

            }
        }

        //TODO: good time to learn to use this properly rather than lazy shit below
        String SecureStringToString(SecureString value)
        {
            IntPtr valuePtr = IntPtr.Zero;
            try
            {
                valuePtr = Marshal.SecureStringToGlobalAllocUnicode(value);
                return Marshal.PtrToStringUni(valuePtr);
            }
            finally
            {
                Marshal.ZeroFreeGlobalAllocUnicode(valuePtr);
            }
        }
    }
